let runtimeLanguage: LanguagePreference = defaultLanguage;

export function setRuntimeLanguage(language: LanguagePreference) {
  runtimeLanguage = language;
}

export function getRuntimeLanguage() {
  return runtimeLanguage;
}

export function translateMessage(
  language: LanguagePreference,
  key: string,
  params?: TranslationParams
) {
  const template = messages[language]?.[key] || englishMessages[key] || key;

  if (!params) {
    return template;
  }

  return Object.entries(params).reduce(
    (output, [paramKey, value]) => output.replaceAll(`{{${paramKey}}}`, String(value)),
    template
  );
}

export function translateRuntimeMessage(key: string, params?: TranslationParams) {
  return translateMessage(runtimeLanguage, key, params);
}
